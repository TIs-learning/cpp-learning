#include <iostream>
using namespace std;


int main() 
{
	// Ada syarat, Ada aksi 
	// for(inisialisasi, loop kondisi, increment) {statement}
	// 		
	// 
	cout << "Awal Looping" << endl;
	for (int a = 6; a <= 17; a+=2) { // bisa nambah argumen variabel lagi
		cout << a << endl;
	}
	
		
	cout << "Sudah Selesai" << endl;
	return 0;
}
