#include <iostream>
using namespace std;


int main(){
	
	int a = 3;
	int b = 3;
	
	bool hasil, hasil2;
	
	// Comparison, relation expression
	
	// equivalent ==
	cout << (a == b) << endl;
	
	// not equivalent !=
	hasil2 = (a != b);
	cout << hasil2 << endl;
	
	// less than <
	hasil2 = (a < b);
	cout << hasil2 << endl;
	
	// more than >
	hasil2 = (a > b);
	cout << hasil2 << endl;
	
	// less than equivalent <=
	hasil2 = (a <= b);
	cout << hasil2 << endl;
	
	// more than equivalent >=
	hasil2 = (a >= b);
	cout << hasil2 << endl;
	
	return 0;
}
